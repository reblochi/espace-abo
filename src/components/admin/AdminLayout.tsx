export function AdminLayout({ children, role }: Props) {
  const pathname = usePathname();
  const { user, logout } = useAuth();

  const isAdmin = role === 'ADMIN';
  const visibleItems = navItems.filter((item) => !item.adminOnly || isAdmin);

  const isActive = (href: string) =>
    href === '/gestion'
      ? pathname === '/gestion'
      : pathname.startsWith(href);

  return (
    <div className="min-h-screen bg-gray-50">
      {/* Header */}
      <header className="bg-white shadow-sm sticky top-0 z-30 border-b border-gray-200">
        <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8">
          <div className="flex justify-between items-center h-14">
            <div className="flex items-center gap-3">
              <Link href="/gestion" className="font-semibold text-gray-900">
                Admin
              </Link>
              <span className={cn(
                'text-xs px-2 py-0.5 rounded-full font-medium',
                isAdmin ? 'bg-red-100 text-red-700' : 'bg-blue-100 text-blue-700'
              )}>
                {isAdmin ? 'Admin' : 'Agent'}
              </span>
            </div>
            <div className="flex items-center gap-4">
              <span className="text-sm text-gray-500">{user?.email}</span>
              <Link href="/espace-membre" className="text-sm text-blue-600 hover:text-blue-800">
                Espace membre
              </Link>
              <button
                onClick={() => logout()}
                className="text-sm text-gray-600 hover:text-gray-900"
              >
                Déconnexion
              </button>
            </div>
          </div>
        </div>
      </header>

      <div className="max-w-7xl mx-auto px-4 sm:px-6 lg:px-8 py-6">
        <div className="flex gap-8">
          {/* Sidebar */}
          <aside className="w-56 flex-shrink-0">
            <nav className="bg-white rounded-lg shadow-sm p-3 sticky top-20">
              <ul className="space-y-1">
                {visibleItems.map((item) => (
                  <li key={item.href}>
                    <Link
                      href={item.href}
                      className={cn(
                        'flex items-center gap-3 px-3 py-2 rounded-md text-sm font-medium transition-colors',
                        isActive(item.href)
                          ? 'bg-blue-50 text-blue-700'
                          : 'text-gray-600 hover:bg-gray-50 hover:text-gray-900'
                      )}
                    >
                      <span className={cn(
                        'flex-shrink-0',
                        isActive(item.href) ? 'text-blue-600' : 'text-gray-400'
                      )}>
                        {item.icon}
                      </span>
                      {item.name}
                    </Link>
                  </li>
                ))}
              </ul>
            </nav>
          </aside>

          {/* Main content */}
          <main className="flex-1 min-w-0">
            {children}
          </main>
        </div>
      </div>
    </div>
  );
}
